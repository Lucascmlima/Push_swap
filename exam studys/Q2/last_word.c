#include <unistd.h>

int main(int ac, char *av[])
{
	int i;
	
	i = -1;
	if (ac == 2)
	{
		while (av[1][++i]);
		i--;
		while(i >= 0 && (av[1][i] == ' ' || (av[1][i] >= 9 && av[1][i] <= 13)))	
			i--;
		while(i >= 0 && !(av[1][i] == ' ' || (av[1][i] >= 9 && av[1][i] <= 13)))
			i--;
		i++;
		while(av[1][i] && !(av[1][i] == ' ' || (av[1][i] >= 9 && av[1][i] <= 13)))
			write(1, &av[1][i++], 1);
	}
	write(1, "\n", 1);
	return (0);
}